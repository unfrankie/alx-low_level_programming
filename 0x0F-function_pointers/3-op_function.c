#include "3-calc.h"

/**
  * op_add - calculate sum of a and b
  * @a: first integer
  * @b: second integer
  * Return: sum of a and b
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - calculate subtraction of a from b
  * @a: 1st integer
  * @b: 2nd integer
  * Return: subtraction of a from b
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - calculate multuplication of a & b
  * @a: 1st integer
  * @b: 2nd integer
  * Return: product of a and b
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - calculate a divided by b
  * @a: 1st integer
  * @b: 2nd integer
  * Return: result division of a by b
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - calculate a modulo b
  * @a: 1st integer
  * @b: 2nd integer
  * Return: a modulo b
  */

int op_mod(int a, int b)
{
	return (a % b);
}
